package br.com.pitagras;

public class CalculadoraBasica {
    public double adicao(double parcelaA, double parcelaB) {
        return parcelaA + parcelaB;
    }

    public double subtracao(double parcelaA, double parcelaB) {
        return parcelaA - parcelaB;
    }

    public boolean ehMaior(double a, double b) {
        return a > b;
    }
}
